<template>
  <div class="dashboard">
    <!-- تنبيه الاشتراك المنتهي -->
    <div
      v-if="showExpiredMessage"
      class="alert alert-warning mb-4 p-3 text-end"
      style="font-size: 1rem"
    >
      <template v-if="!graceExpired">
        <strong class="d-block mb-2"
          >⚠️ لقد انتهت مدة الاشتراك منذ تاريخ :
          <span class="text-danger fw-bold">{{
            arabicDate(subscriptionEnd)
          }}</span>
        </strong>
        <span></span>
        <span>سوف يبقى المنيو <strong>فعالاً</strong> لغاية</span>
        <strong class="text-dark">{{ arabicDate(graceEndDateRaw) }}</strong>
        <span>، بعدها سيتوقف تلقائيًا.</span><br />
        <span class="text-muted"
          >يرجى التواصل مع الإدارة لضمان استمرارية الخدمة.</span
        >
      </template>

      <template v-else>
        <strong class="d-block mb-2 text-danger"
          >⛔ تم إيقاف المنيو بتاريخ :
          <span class="fw-bold text-danger">{{
            arabicDate(graceEndDateRaw)
          }}</span>
        </strong>
        <span></span>
        <span>انتهت مدة الاشتراك ولم يتم التجديد خلال المهلة المحددة.</span
        ><br />
        <span class="text-muted">يرجى التجديد لإعادة تفعيل المنيو.</span>
      </template>
    </div>

    <!-- العنوان -->
    <h2 class="title">مرحباً بك من جديد يا {{ username }} 👋</h2>
    <p class="date">اليوم: {{ todayDate }}</p>

    <!-- الإحصائيات -->
    <div class="stats-grid">
      <div v-for="item in statItems" :key="item.key" class="stat-card">
        <div :class="['icon', item.color]">
          <i :class="item.icon"></i>
        </div>
        <div class="content">
          <h4>{{ item.label }}</h4>
          <p>{{ item.value }}</p>
        </div>
      </div>
    </div>
  </div>

  <h4 class="mt-5 mb-3">مزايا الباقة الحالية :</h4>

  <div class="plan-features">
    <div class="feature-card name-card">
      <div class="circle-content">
        <div class="label">اسم الباقة</div>
        <div class="value">{{ plan.name }}</div>
      </div>
    </div>

    <div class="feature-card">
      <CircleProgress
        :used="itemCount"
        :total="plan.itemLimit === 'unlimited' ? 'unlimited' : plan.itemLimit"
        label="الأصناف"
      />
    </div>

    <div class="feature-card">
      <CircleProgress
        :used="sectionCount"
        :total="
          plan.sectionLimit === 'unlimited' ? 'unlimited' : plan.sectionLimit
        "
        label="الأقسام"
      />
    </div>

    <div class="feature-card" :class="{ enabled: plan.hasDashboard }">
      <i
        :class="
          plan.hasDashboard ? 'bi bi-check-circle-fill' : 'bi bi-x-circle-fill'
        "
      ></i>
      <div class="label">لوحة التحكم</div>
    </div>

    <div class="feature-card" :class="{ enabled: plan.hasLogo }">
      <i
        :class="
          plan.hasLogo ? 'bi bi-check-circle-fill' : 'bi bi-x-circle-fill'
        "
      ></i>
      <div class="label">تخصيص الشعار</div>
    </div>
  </div>
</template>

<script>
import api from "../../axios";
import CircleProgress from "@/components/CircleProgress.vue";

export default {
  name: "ClientDashboardView",
  components: { CircleProgress },
  data() {
    return {
      username: null,
      sectionCount: 0,
      itemCount: 0,
      subscriptionEnd: null,
      daysLeft: null,
      showExpiredMessage: false,
      graceExpired: false,
      plan: {
        name: "",
        sectionLimit: 0,
        itemLimit: 0,
        hasDashboard: false,
        hasLogo: false,
      },
    };
  },
  computed: {
    graceEndDateRaw() {
      if (!this.subscriptionEnd) return null;
      const end = new Date(this.subscriptionEnd);
      end.setDate(end.getDate() + 7);
      return end;
    },
    todayDate() {
      return new Date().toLocaleDateString("ar-EG", {
        weekday: "long",
        year: "numeric",
        month: "long",
        day: "numeric",
      });
    },
    statItems() {
      const days = this.daysLeft;
      const isExpired = days < 0;
      const isTodayLast = days === 0;
      const isActive = days > 0;

      return [
        {
          key: "sectionCount",
          label: "عدد الأقسام",
          icon: "bi bi-collection",
          color: "bg-blue",
          value: this.sectionCount,
        },
        {
          key: "itemCount",
          label: "عدد الأصناف",
          icon: "bi bi-basket2",
          color: "bg-green",
          value: this.itemCount,
        },
        {
          key: "subscriptionEnd",
          label: "تاريخ انتهاء الاشتراك",
          icon: "bi bi-calendar-event",
          color: "bg-orange",
          value: this.subscriptionEnd || "غير متوفر",
        },
        {
          key: "daysLeft",
          label: isActive
            ? "تبقى على انتهاء الاشتراك"
            : isTodayLast
            ? "اليوم هو آخر يوم للاشتراك"
            : "لقد مضى على انتهاء اشتراكك",
          icon: isActive
            ? "bi bi-hourglass-split"
            : isTodayLast
            ? "bi bi-exclamation-triangle"
            : "bi bi-x-octagon",
          color: isActive
            ? "bg-red"
            : isTodayLast
            ? "bg-warning"
            : "bg-dark expired-alert", // ← فقط في حال انتهى الاشتراك تمامًا
          value: days !== null ? (isExpired ? days * -1 : days) + " يوم" : "--",
        },
      ];
    },
  },
  methods: {
    arabicDate(date) {
      if (!date) return "غير متوفر";
      const d = new Date(date);
      return d.toLocaleDateString("ar-EG", {
        year: "numeric",
        month: "2-digit",
        day: "2-digit",
      });
    },
  },
  async mounted() {
    try {
      const res = await api.get("/dashboard");
      this.username = res.data.username;
      this.sectionCount = res.data.sectionCount;
      this.itemCount = res.data.itemCount;
      this.subscriptionEnd = res.data.subscriptionEnd;
      this.daysLeft = res.data.daysLeft;
      this.showExpiredMessage = res.data.subscriptionExpired;
      this.graceExpired = this.daysLeft < -7; // تجاوز 7 أيام بعد الانتهاء
      this.plan = res.data.level;
    } catch (err) {
      console.error("فشل تحميل البيانات:", err);
    }
  },
};
</script>

